class ByteConv
{
	public static void main(String[] args)
	{
		byte a=10;

		short b=a;
		int c=a;
		long d=a;
		float e=a;
		double f=a;
		System.out.println("Short" +b);
		System.out.println("Int "+c);
		System.out.println("long "+d);
		System.out.println("float "+e);
		System.out.println("double "+f);

	}
}
class Conditional
{
	public static void main(String[] args)
	{
		System.out.println(10<20); //true
		System.out.println(99>200); //false
		System.out.println(10<=9); //fase
		System.out.println(20>=5); //true
		System.out.println(20==20);//true
//		System.out.println(true<false); //CTE
		System.out.println(false==false); //true
		System.out.println(true==true);//true
		System.out.println(0==0.0);//true
		System.out.println((27/7.0)==(22/7));//false
		System.out.println(22.0/7.0==22.0f/7.0f);//false
		System.out.println(20.0f==20.0);
	}
}

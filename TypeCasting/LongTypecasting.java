class LongTypecasting
{
	public static void main(String[] args)
	{
		long a=11115472;
		int b= (int)a;
		short c=(short)a;
		char d=(char)a;
		byte e=(byte)a;

		System.out.println("int : "+b);
		System.out.println("short : "+c);
		System.out.println("char : "+d);
		System.out.println("byte : "+e);

	}
}